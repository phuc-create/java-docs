package Variables;

public class Variables {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		String str_var = "Nguyen Huu Phuc";
		char char_var = '\u0000';
		int number_var = 140920;
		float float_var = 1.28f;
		double double_var = 0.99;
		boolean boolean_var = true || false;
		byte byte_var = 10;
		short short_var = -10000; //from -32,768 to 32,767
		
		System.out.println(str_var);
		System.out.println(char_var);
		System.out.println(number_var);
		System.out.println(float_var);
		System.out.println(double_var);
		System.out.println(boolean_var);
		System.out.println(byte_var);
		System.out.println(short_var);

	}

}
